package school.mjc.stage0.module3.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        char c = 'c';
        char h = 'h';
        char a = 'a';
        char r = 'r';

        int k = c;
        int j = h;
        int l = a;
        int g = r;

        System.out.println(k);
        System.out.println(j);
        System.out.println(l);
        System.out.println(g);
    }

}
